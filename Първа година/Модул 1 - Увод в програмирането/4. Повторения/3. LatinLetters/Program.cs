using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _3.LatinLetters
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.Write("Latin alphabet: ");
            for (char a = 'a'; a <= 'z'; a++)
            {
                Console.Write(a + " ");
            }
        }
    }
}
